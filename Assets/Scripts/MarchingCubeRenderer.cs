        private Vector3 getPointPosition(float x, float y , float z, float cubeSize)
        {
            return new Vector3(x, y, z) / cubeSize - offset;
        }

        void RenderSurface()
        {
            Mesh m = new Mesh();
            meshF.mesh = m;
            List<Vector3> V = new List<Vector3>();
            List<int> T = new List<int>();
            List<Vector3> N = new List<Vector3>();
            //looping over the Cells
            for (int y = 0; y < dataSize.y - 1; ++y)
            {
                for (int z = 0; z < dataSize.z - 1; ++z)
                {
                    for (int x = 0; x < dataSize.x - 1; ++x)
                    {
                        CubeCell cell = GetCell(x, y, z,false);
                        List<Triangle> triangles;
                        int nbTriangles = Polygonise(cell, isoLevel, out triangles);
                        if (nbTriangles != 0)
                        {
                            for (int i = 0; i < nbTriangles; ++i)
                            {
                                int currentVertexAmount = V.Count;
                                V.Add(triangles[i].points[0]);
                                V.Add(triangles[i].points[1]);
                                V.Add(triangles[i].points[2]);
                                T.Add(currentVertexAmount + 2);
                                T.Add(currentVertexAmount + 1);
                                T.Add(currentVertexAmount + 0);
                                Vector3 normal = GetNormal(triangles[i].points);
                                N.Add(normal);
                                N.Add(normal);
                                N.Add(normal);
                            }
                            cursor = new Vector3(x, y, z);
                        }
                    }
                }
            }
            m.vertices = V.ToArray();
            m.triangles = T.ToArray();
            m.normals = N.ToArray();
            meshF.mesh = m;
            cursor = Vector3.zero;
        }

        IEnumerator RenderSurfaceStepByStep()
        {
            Mesh m = new Mesh();
            meshF.mesh = m;
            WaitForSeconds wait = new WaitForSeconds(0.001f);
            //looping over the Cells
            for (int y = 0; y < dataSize.y - 1; ++y)
            {
                for (int z = 0; z < dataSize.z - 1; ++z)
                {
                    for (int x = 0; x < dataSize.x - 1; ++x)
                    {
                        CubeCell cell = GetCell(x, y, z,false);
                        List<Triangle> triangles;
                        int nbTriangles = Polygonise(cell, isoLevel, out triangles);
                        if (nbTriangles != 0)
                        {
                            List<Vector3> V = m.vertices.ToList();
                            List<int> T = m.triangles.ToList();
                            List<Vector3> N = m.normals.ToList();
                            for (int i = 0; i < nbTriangles; ++i)
                            {
                                int currentVertexAmount = V.Count;
                                V.Add(triangles[i].points[0]);
                                V.Add(triangles[i].points[1]);
                                V.Add(triangles[i].points[2]);
                                T.Add(currentVertexAmount + 2);
                                T.Add(currentVertexAmount + 1);
                                T.Add(currentVertexAmount + 0);
                                Vector3 normal = GetNormal(triangles[i].points);
                                N.Add(normal);
                                N.Add(normal);
                                N.Add(normal);
                            }
                            m.vertices = V.ToArray();
                            m.triangles = T.ToArray();
                            m.normals = N.ToArray();
                            cursor = new Vector3(x, y, z);
                            yield return wait;
                        }
                    }
                }
            }
            meshF.mesh = m;
            cursor = Vector3.zero;
        }

        private Vector3 GetNormal(Vector3[] points)
        {
            Vector3 U = points[1] - points[0];
            Vector3 V = points[2] - points[0];
            Vector3 normal = new Vector3();
            normal.x = (U.y * V.z) - (U.z * V.y);
            normal.y = (U.z * V.x) - (U.x * V.z);
            normal.z = (U.x * V.y) - (U.y * V.x);
            return normal.normalized;
        }

        private CubeCell GetCell(int x, int y, int z,bool noData)
        {
            CubeCell cell = new CubeCell();
            cell.p[0] = new Vector3(x, y, z)/density - offset;
            cell.p[1] = new Vector3(x, y + 1, z)/density - offset;
            cell.p[2] = new Vector3(x + 1, y + 1, z)/density - offset;
            cell.p[3] = new Vector3(x + 1, y, z)/density - offset;
            cell.p[4] = new Vector3(x, y, z + 1)/density - offset;
            cell.p[5] = new Vector3(x, y + 1, z + 1)/density - offset;
            cell.p[6] = new Vector3(x + 1, y + 1, z + 1)/density - offset;
            cell.p[7] = new Vector3(x + 1, y, z + 1)/density - offset;
            if (!noData)
            {
                cell.v[0] = data.getPointValue(x, y, z);
                cell.v[1] = data.getPointValue(x, y + 1, z);
                cell.v[2] = data.getPointValue(x + 1, y + 1, z);
                cell.v[3] = data.getPointValue(x + 1, y, z);
                cell.v[4] = data.getPointValue(x, y, z + 1);
                cell.v[5] = data.getPointValue(x, y + 1, z + 1);
                cell.v[6] = data.getPointValue(x + 1, y + 1, z + 1);
                cell.v[7] = data.getPointValue(x + 1, y, z + 1);
            }
            
            return cell;
        }

        //Given a grid cell and an isolevel, calculate the triangular
        //facets required to represent the isosurface through the cell.
        //Return the number of triangular facets, the array "triangles"
        //will be loaded up with the vertices at most 5 triangular facets.
        //0 will be returned if the grid cell is either totally above
        //of totally below the isolevel.
        private int Polygonise(CubeCell cell, float isolevel, out List<Triangle> triangles)
        {
            triangles = new List<Triangle>();

            int i = 0;

            //   Determine the index into the edge table which
            //   tells us which vertices are inside of the surface

            int cubeindex = 0;
            if (cell.v[0] < isolevel) cubeindex |= 1;
            if (cell.v[1] < isolevel) cubeindex |= 2;
            if (cell.v[2] < isolevel) cubeindex |= 4;
            if (cell.v[3] < isolevel) cubeindex |= 8;
            if (cell.v[4] < isolevel) cubeindex |= 16;
            if (cell.v[5] < isolevel) cubeindex |= 32;
            if (cell.v[6] < isolevel) cubeindex |= 64;
            if (cell.v[7] < isolevel) cubeindex |= 128;


            // Cube is entirely in/out of the surface 
            if (edgeTable[cubeindex] == 0)
                return 0;

            // Find the vertices where the surface intersects the cube 


            Vector3[] vertlist = new Vector3[12];
            int edge = MarchingCubeRenderer.edgeTable[cubeindex];
            if ((edge & 1) > 0)
                vertlist[0] =
                    VertexInterp(isolevel, cell.p[0], cell.p[1], cell.v[0], cell.v[1]);
            if ((edge & 2) > 0)
                vertlist[1] =
                    VertexInterp(isolevel, cell.p[1], cell.p[2], cell.v[1], cell.v[2]);
            if ((edge & 4) > 0)
                vertlist[2] =
                    VertexInterp(isolevel, cell.p[2], cell.p[3], cell.v[2], cell.v[3]);
            if ((edge & 8) > 0)
                vertlist[3] =
                    VertexInterp(isolevel, cell.p[3], cell.p[0], cell.v[3], cell.v[0]);
            if ((edge & 16) > 0)
                vertlist[4] =
                    VertexInterp(isolevel, cell.p[4], cell.p[5], cell.v[4], cell.v[5]);
            if ((edge & 32) > 0)
                vertlist[5] =
                    VertexInterp(isolevel, cell.p[5], cell.p[6], cell.v[5], cell.v[6]);
            if ((edge & 64) > 0)
                vertlist[6] =
                    VertexInterp(isolevel, cell.p[6], cell.p[7], cell.v[6], cell.v[7]);
            if ((edge & 128) > 0)
                vertlist[7] =
                    VertexInterp(isolevel, cell.p[7], cell.p[4], cell.v[7], cell.v[4]);
            if ((edge & 256) > 0)
                vertlist[8] =
                    VertexInterp(isolevel, cell.p[0], cell.p[4], cell.v[0], cell.v[4]);
            if ((edge & 512) > 0)
                vertlist[9] =
                    VertexInterp(isolevel, cell.p[1], cell.p[5], cell.v[1], cell.v[5]);
            if ((edge & 1024) > 0)
                vertlist[10] =
                    VertexInterp(isolevel, cell.p[2], cell.p[6], cell.v[2], cell.v[6]);
            if ((edge & 2048) > 0)
                vertlist[11] =
                    VertexInterp(isolevel, cell.p[3], cell.p[7], cell.v[3], cell.v[7]);

            int nTriangle = 0;
            for (i = 0; triTable[cubeindex, i] != -1; i += 3)
            {
                Triangle tri = new Triangle();
                tri.points[0] = vertlist[triTable[cubeindex, i]];
                tri.points[1] = vertlist[triTable[cubeindex, i + 1]];
                tri.points[2] = vertlist[triTable[cubeindex, i + 2]];
                triangles.Add(tri);
                nTriangle++;
            }

            return nTriangle;
        }

        //Linearly interpolate the position where an isosurface cuts
        //an edge between two vertices, each with their own scalar value

        Vector3 VertexInterp(float isolevel, Vector3 p1, Vector3 p2, float valp1, float valp2)
        {
            if (Mathf.Abs(isolevel - valp1) < 0.00001)
                return p1;
            if (Mathf.Abs(isolevel - valp2) < 0.00001)
                return p2;
            if (Mathf.Abs(valp1 - valp2) < 0.00001)
                return p1;

            Vector3 p = new Vector3();
            float mu = (isolevel - valp1) / (valp2 - valp1);
            p.x = p1.x + mu * (p2.x - p1.x);
            p.y = p1.y + mu * (p2.y - p1.y);
            p.z = p1.z + mu * (p2.z - p1.z);

            return p;
        }


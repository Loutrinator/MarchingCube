        public void ResetData()
        {
            scale = new int[3]{(int)width,(int)height,(int)depth};
            dataSize = new Vector3((int) (scale[0] * density), (int) (scale[1] * density), (int) (scale[2] * density));
            data = new MarchingCubeData((int)dataSize.x, (int)dataSize.y, (int)dataSize.z);
            offset = new Vector3(scale[0] / 2f, scale[1] / 2f, scale[2] / 2f);
            ComputeData();
            if (animateOnPlay && Application.isPlaying)
            {
                StartCoroutine(RenderSurfaceStepByStep());
                return;
            }
            RenderSurface();
            
        }

        private void Start()
        {
            ResetData();
        }

        private void ComputeData()
        {
            float d = density == 0 ? 0.0001f : density;
            for (int z = 0; z < dataSize.z; ++z)
            {
                for (int x = 0; x < dataSize.x; ++x)
                {
                    float perlin = 0;
                    Vector2 pos = new Vector2(x/d/width , z/d/depth);
                    foreach (var layer in perlinlayers)
                    {
                        perlin += (-0.5f + Mathf.PerlinNoise(pos.x * layer.scale, pos.y * layer.scale)) * layer.intensity;
                    }

                    perlin *= perlinHeight;
                    perlin += perlinFloor;
                    for (int y = 0; y < dataSize.y; ++y)
                    {
                        float terrainHeight = getPointPosition(x, y, z,d).y;
                        float value = Mathf.Clamp(perlin - terrainHeight,0,1);
                        data.setPointValue(x, y, z, value);
                    }
                }
            }
        }

